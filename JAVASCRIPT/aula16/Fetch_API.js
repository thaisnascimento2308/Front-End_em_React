// Fetch API
// Fetch API é uma API do Browser.
// Fetch API é uma interface JavaScript que permite fazer requisições HTTP de forma assíncrona.
// Ela é usada para buscar recursos da web, como arquivos JSON, imagens, vídeos, etc.
// A Fetch API é baseada em Promises, o que significa que ela permite lidar com operações assíncronas de forma mais fácil e legível do que as callbacks tradicionais.
